package sk.cyklosoft.hrmservice.vo;


public class Team {
	
    private Long teamId;
    private String name;

	public Long getTeamId() {
		return teamId;
	}

	public void setTeamId(Long teamId) {
		this.teamId = teamId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
